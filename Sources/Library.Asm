SEG      MACRO
         DO      ]#=1
         .RSRC   CODE,]1
         ELSE
         .RSRC   CODE,]1,]2
         FIN
         DO      ]1≠0
         .DATA   /SEG_]1-SEG0
         .DATA   /(END_]1-SEG_]1)/8
SEG.]1
         ELSE
JP_OFF   .EQU    ]4
         .DATA   JP_OFF+END0-SEG0
         .DATA   ]3
         .DATA   END0-SEG0
         .DATA   JP_OFF
         FIN
         ENDM
SJMP     MACRO
         JMP     J_]1-SEG0+JP_OFF+2(A5)
         ENDM
SJSR     MACRO
         JSR     J_]1-SEG0+JP_OFF+2(A5)
         ENDM
JP       MACRO
J_]1     .DATA    /]1-SEG.]2,/$3F3C,/]2,/$A9F0
         ENDM
